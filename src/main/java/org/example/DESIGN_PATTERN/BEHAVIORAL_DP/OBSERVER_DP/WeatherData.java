package org.example.DESIGN_PATTERN.BEHAVIORAL_DP.OBSERVER_DP;

import java.util.ArrayList;
import java.util.List;


//Implement the Subject: Create a class that implements the Subject interface.
// This class will maintain a list of observers and notify them when a change occurs.
public class WeatherData implements Subject {
    private List<Observer> observers;
    private float temperature;
    private float humidity;
    private float pressure;

    public WeatherData() {
        observers = new ArrayList<>();
    }

    @Override
    public void registerObserver(Observer observer) {
        observers.add(observer);
    }

    @Override
    public void removeObserver(Observer observer) {
        observers.remove(observer);
    }

    @Override
    public void notifyObservers() {
        for (Observer observer : observers) {
            observer.update(temperature, humidity, pressure);
        }
    }

    // Other methods to update the weather data and trigger notifications
    public void setMeasurements(float temperature, float humidity, float pressure) {
        this.temperature = temperature;
        this.humidity = humidity;
        this.pressure = pressure;
        notifyObservers();
    }
}
